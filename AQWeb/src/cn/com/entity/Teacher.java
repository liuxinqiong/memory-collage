package cn.com.entity;

/**
 * 老师对象
 * 
 * @author TYB
 * 
 */
public class Teacher {
	private int teaNo;
	private int profeNo;
	private String teaName;
	private String teaPwd;
	private String teaGrade;
	private String teaDescription;
	private int points;
	private int teaSatisfaction;
	private int status;
	private String teaPic;
	private int isDel;

	public int getTeaNo() {
		return teaNo;
	}

	public void setTeaNo(int teaNo) {
		this.teaNo = teaNo;
	}

	public int getProfeNo() {
		return profeNo;
	}

	public void setProfeNo(int profeNo) {
		this.profeNo = profeNo;
	}

	public String getTeaName() {
		return teaName;
	}

	public void setTeaName(String teaName) {
		this.teaName = teaName;
	}

	public String getTeaPwd() {
		return teaPwd;
	}

	public void setTeaPwd(String teaPwd) {
		this.teaPwd = teaPwd;
	}

	public String getTeaGrade() {
		return teaGrade;
	}

	public void setTeaGrade(String teaGrade) {
		this.teaGrade = teaGrade;
	}

	public String getTeaDescription() {
		return teaDescription;
	}

	public void setTeaDescription(String teaDescription) {
		this.teaDescription = teaDescription;
	}

	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}

	public int getTeaSatisfaction() {
		return teaSatisfaction;
	}

	public void setTeaSatisfaction(int teaSatisfaction) {
		this.teaSatisfaction = teaSatisfaction;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getTeaPic() {
		return teaPic;
	}

	public void setTeaPic(String teaPic) {
		this.teaPic = teaPic;
	}

	public int getIsDel() {
		return isDel;
	}

	public void setIsDel(int isDel) {
		this.isDel = isDel;
	}

}
